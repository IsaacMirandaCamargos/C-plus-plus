#include <iostream>
#include <cmath>

using namespace std;

int main()
{

    int N;
    cin >> N;

    cout << pow(N, 2) << endl;  // ELEVA UM NUMERO
    cout << sqrt(N) << endl;    // RAIZ DE UM NUMERO
    cout << floor(1.8) << endl; // ARREDONDA UM NUMERO PRA BAIXO
    cout << ceil(1.1) << endl;  // ARREDDONDA UM NUMERO PARA CIMA
}